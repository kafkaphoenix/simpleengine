#pragma once
#include <GLFW/glfw3.h>

#include <string>

class EventBus;

class Window {
   public:
    Window(int width, int height, const std::string& title, EventBus* eventBus);
    ~Window();

    void pollEvents() const;
    void swapBuffers() const;
    bool shouldClose() const;
    void toggleFullscreen();
    void onFramebufferResize(int width, int height);
    void onKeyEvent(int key, int scancode, int action, int mods);
    void onMouseButtonEvent(int button, int action, int mods);
    void onMouseMove(double xpos, double ypos);
    void onScroll(double xoffset, double yoffset);
    void onWindowFocus(bool focused);
    void setTitle(const std::string& title);
    const std::string& baseTitle() const { return m_BaseTitle; }
    void setVsync(bool enabled);
    void setGlDebugNotifications(bool enabled);

    GLFWwindow* native() const { return m_Window; }

   private:
    friend void windowGlDebugCallback(unsigned int source, unsigned int type,
                                      unsigned int id, unsigned int severity,
                                      int length, const char* message, const void* userParam);
    void initGlfw();
    void setupGlfwHints();
    void createWindow(int width, int height, const std::string& title);
    void initGlad();
    void setupGlDebug();
    void setupCallbacks();
    void setupInitialFramebuffer(int width, int height);
    void setupInputMode();
    GLFWwindow* m_Window;
    EventBus* m_EventBus = nullptr;
    bool m_IsFullscreen = false;
    int m_WindowedWidth;
    int m_WindowedHeight;
    int m_WindowedPosX;
    int m_WindowedPosY;
    std::string m_BaseTitle;
    int m_LastFramebufferWidth = 0;
    int m_LastFramebufferHeight = 0;
    bool m_GlDebugNotifications = false;
};
